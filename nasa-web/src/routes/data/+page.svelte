<h3>Principales Sectores que Contribuyen a las Emisiones de CO2</h3>
<p>
	Según los datos del IPCC, los principales sectores de la economía mundial que contribuyen a las
	emisiones de CO2 son los siguientes:
</p>
<ul>
	<li>Generación de calor y electricidad (25%)</li>
	<li>Agricultura, silvicultura y uso de la tierra (24%)</li>
	<li>Industria (21%)</li>
	<li>Transporte (14%)</li>
	<li>Otra producción de energía (10%)</li>
	<li>Edificios (6%)</li>
</ul>
<p>
	Para conocer con mayor precisión la distribución de las emisiones de CO2 por sectores, debemos
	saber qué sector consume qué cantidad de energía y cuáles emiten o no gases de efecto invernadero
	adicionales. El problema es que estos datos son bastante difíciles de sintetizar con precisión.
</p>

