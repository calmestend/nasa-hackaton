<h1>Acerca de los límites planetarios</h1>
<h2>Umbrales que no se deben traspasar:</h2>
<p>
	Esta pregunta se la ha formulado un grupo de 29 científicos, encabezados por Johan Rockström, de
	la Universidad de Estocolmo. La idea que les mueve es que la acción antropógena ha alcanzado tales
	proporciones que no puede excluirse un cambio global abrupto. Para evitarlo, sugieren disponer de
	unos indicadores que señalen los límites biofísicos que no debemos pasar para que no se produzcan
	procesos no lineales de consecuencias potencialmente catastróficas.
</p>

<h2>Nueve Límites:</h2>
<p>
	De la investigación se detectaron nueve límites, de los cuales, siete de ellos ponen cifras a los
	umbrales que deberían respetarse. Estos siete se relacionan con la concentración de CO2 en la
	atmósfera, la acidificación oceánica, la concentración de ozono estratosférico, la fijación de
	nitrógeno y el vertido anual de fósforo al mar, el consumo de agua dulce, la proporción de tierras
	cultivadas y la pérdida de la diversidad biológica. Los dos restantes, más difíciles de
	cuantificar con un sólo indicador, son la carga de aerosoles y la contaminación química.
</p>

<h2>¿Cerca de los límites?</h2>
<p>
	Los autores consideran que ya hemos transgredido tres de estos umbrales (CO2, pérdida de
	diversidad y nitrógeno), ya que todos estos límites son interdependientes, por lo que rebasar uno
	de ellos puede arrastrarnos a pasar también otros.
</p>

<h2>La propuesta:</h2>
<p>
	La propuesta es trabajar sobre el concepto de límites planetarios para definir el marco de
	seguridad para las sociedades humanas. Además, se reconocen las dificultades para establecer estos
	límites, porque nuestros conocimientos del sistema pueden ser insuficientes, porque es muy difícil
	comprender el funcionamiento de los sistemas complejos con sus interacciones no lineales, y porque
	los efectos de la transgresión de un límite dependen también de la resiliencia de nuestras
	sociedades.
</p>

<h2>Dificultades con los indicadores</h2>
<p>
	Lo que pide la mayoría de los ecólogos es que se den indicadores precisos como el PIB. Sin
	embargo, muchos de estos índices agregan muchas cosas, pero su interpretación está lejos de ser
	sencilla, pero que en economía se aplican muchas veces con demasiado dogmatismo. Disponer de este
	tipo de indicadores macroeconómicos no ha evitado las crisis. Algunos economistas prudentes claman
	por volver a prestar más atención a la microeconomía. Quizás esto pueda servirnos de aviso.
</p>
<p>
	Esto no significa que sean completamente inútiles. Podrían tener cierta función pedagógica. Pero
	también pueden ser engañosos. No es tanto que se puedan usar mejor o peor, como el hecho en sí de
	que, cuando tenemos cifras, tendemos a creer demasiado en ellas.
</p>

<h1>El Sistema Planetario</h1>
<p>
	El sistema planetario es de una complejidad enorme. También lo es el cuerpo humano y tomarle la
	temperatura sigue siendo un indicador útil, pero nos advierte de que algo no funciona bien, o no
	como se debería esperar.
</p>

<h2>¿Son tan débiles los límites que se nos proponen para el sistema planetario?</h2>
<p>
	Puede que no. Para empezar, tiene una ventaja muy importante: no son valores agregados. Cada uno
	se refiere a una variable específica. Por ejemplo, la situación del ciclo del carbono y el cambio
	climático se pondera en relación a un valor límite de 350 ppm de CO2 en la atmósfera. Una sola
	variable, cierto, para una serie de procesos muy complicados, y esto también es un inconveniente.
</p>

<p>
	Para empezar hay otros gases de efecto invernadero además del CO2. Luego, las relaciones entre
	cada variable y los procesos planetarios son poco menos que indescifrables. Hay indicios para
	pensar que un aumento del CO2 ha ido asociado a mayores temperaturas; el análisis de las burbujas
	de aire apresadas en la sonde obtenida en los hielos de Vostok ha permitido reconstruir la
	evolución, fluctuando en paralelo, de la concentración del gas y la temperatura hace 400.000 años.
</p>

<p>
	Sin embargo, este paralelismo es menos evidente cuando se considera el aumento del CO2 en las
	últimas décadas y la evolución de la temperatura, que no parece seguir el mismo ritmo. Además, en
	las muestras polares el aumento de la temperatura precede al del CO2, mientras que ahora el
	proceso debería ir al revés, el cambio atmosférico provoca el aumento térmico.
</p>

<h1>Querétaro</h1>
<p>
	Durante el año 2023, el estado de Querétaro tiene la meta de reducir o compensar 2 millones de
	toneladas de dióxido de carbono (CO2), de acuerdo a la Secretaría de Desarrollo Sustentable
	(SEDESU).
</p>
<p>
	La dependencia estatal identificó a 400 empresas industriales que generan emisiones directas a la
	atmósfera, por lo que se han tenido acercamientos para capacitarlas en la obtención del sello y
	que recurren a los mecanismos de compensación.
</p>

<h2>Propuestas</h2>
<p>
	El secretario resaltó que se creó el programa estatal de acciones contra el cambio climático, que
	tiene la finalidad de reducir la generación de gases de efecto invernadero.
</p>
<p>
	De este programa derivan diversas iniciativas e instrumentos para reducir la huella de carbono,
	uno de ellos es el impuesto ambiental, uno de los más caros del país, pero que buscan se traduzca
	en acciones para combatir esas emisiones.
</p>
<p>
	“Un impuesto ambiental que si bien no es el más barato del país, puedo decirlo con certeza, que es
	el más caro, porque buscamos que las empresas que generen emisiones directas a la atmósfera
	contribuyan con las reducciones de sus emisiones, porque no queremos que este impuesto se vuelva
	una renta ambiental, queremos que por el contrario a través de un mecanismo del sello estatal de
	bajas emisiones de carbono podamos compensar las emisiones que se generen en exceso en nuestro
	estado”, planteó.
</p>

<p>
	El sello se otorga a los proyectos ambientales que registren las compañías; un ejemplo es un
	programa que desarrolla el Grupo Ecológico Sierra Gorda y que es susceptible de obtenerlo.
</p>
<p>
	Dicho sello plantea tres opciones: la primera, medición y reducción de emisiones; la segunda,
	medición y compensación de emisiones directas; y la tercera, medición y compensación de emisiones
	indirectas.
</p>

<p>
	Según el INECC, en el 2020, las emisiones de CO2 en Querétaro se derivan principalmente de
	sectores como el transporte, la industria y la generación de energía.
</p>
<p>
	Los datos específicos sobre las toneladas de CO2 emitidas por sector pueden variar, pero las
	tendencias indican un crecimiento en las emisiones debido al aumento de la actividad industrial y
	vehicular.
</p>
<p>
	Por otro lado, en el informe de Calidad de Aire en Querétaro de acuerdo con el Gobierno del Estado
	de Querétaro, en el informe de 2021, se menciona que las concentraciones de CO2 y otros
	contaminantes han mostrado variaciones estacionales, con un incremento notable durante los meses
	de mayor actividad vehicular.
</p>
<p>
	Se destacan medidas implementadas para la reducción de emisiones, como mejoras en el transporte
	público y programas de reforestación.
</p>

<h2>Datos Alarmantes</h2>
<p>
	“Es este año cuando se empieza a cobrar el impuesto, cuando se lanza el sello de bajo carbono y
	que las empresas que estaban generando emisiones tendrán que reducirlas, nuestra meta este año,
	este primer año, es reducir o compensar cerca de 2 millones de toneladas de las 12 millones con
	las que arrancó en 2020 nuestra línea base”, dijo el funcionario.
</p>

<p>
	Querétaro tiene la intención de reducir o compensar 2 millones de toneladas de CO2 para 2023. La
	dependencia estatal ha identificado a 400 empresas industriales que emiten directamente a la
	atmósfera y ha iniciado capacitaciones para ayudarlas a obtener el sello de compensación.
</p>

<h2>CO2 en CDMX</h2>

<h3>Problemática</h3>
<p>
	Se determinaron las emisiones adicionales de dióxido de carbono (CO2) ocasionadas por las obras
	que se realizan en la CETRAM Indios Verdes y que obstruyeron la vialidad, en el norte de la Ciudad
	de México.
</p>
<p>
	Se realizaron recorridos físicos y aforos en el tramo de la carretera federal México-Pachuca y su
	continuación en avenidas Insurgentes Norte; para posteriormente y utilizando factores de emisión,
	estimar las cantidades de CO2 emitido en el tiempo que los vehículos permanecen en el tránsito
	provocado.
</p>
<p>
	Con los datos obtenidos podemos concluir que debido a una deficiente planificación ambiental,
	urbana y de obras viales, así como por la ausencia de medidas de prevención, mitigación y control,
	se estima que se emitieron adicionalmente a la atmósfera del norte de la Ciudad de México, más de
	17 millones de kilogramos de dióxido de carbono. Adicionalmente, se estima la pérdida de más de 48
	millones de horas hombre en el tránsito y el consumo adicional de más de 1.5 millones de litros de
	gasolina y más de 900 mil litros de diésel.
</p>
<p>
	El crecimiento constante y fuera de control de la ciudad, aunado a la falta de planeación
	ambiental, tiene repercusiones negativas en el ambiente y en la salud de sus habitantes, más aún
	cuando hablamos de ciudades consideradas como Megalópolis, como lo es la Ciudad de México.
</p>

<h3>Datos Generales</h3>
<p>
	Las emisiones de GEI para el 2010 en unidades de CO2 equivalente, se estimaron en gigagramos (Gg):
</p>
<ul>
	<li>Bióxido de carbono (CO2) = 65.9%</li>
</ul>
<p>
	Las emisiones de CO2 fueron de 493,450.6 Gg en 2010, contribuyeron en un 65.9% al total del
	inventario y tuvieron un incremento de 23.6% con respecto a 1990. Las emisiones de CO2 en el país
	provienen principalmente de la quema de combustibles fósiles, USCUSS y Procesos Industriales.
</p>

<h3>Emisiones de CO2 en México en 2022</h3>
<p>
	En 2022, las emisiones de CO2 en México han crecido 45,949 megatoneladas, un 10,4% respecto a
	2021. Las emisiones de CO2 en 2022 han sido de 487,774 megatoneladas, lo que coloca a México entre
	los países más contaminantes del ranking de países por emisiones de CO2, formado por 184 países,
	en el que se ordenan de menos a más contaminantes.
</p>
<p>
	Además de sus emisiones totales de CO2 a la atmósfera, que lógicamente dependen, entre otras
	variables, de la población del país, es conveniente analizar el comportamiento de sus emisiones
	por habitante. En la tabla vemos que las emisiones per cápita de CO2 en México han aumentado en
	2022, siendo de 3,56 toneladas por habitante.
</p>
<p>
	Es importante también analizar la "eficiencia medioambiental" del país, es decir, la cantidad de
	emisiones por cada unidad de PIB generada. En el último periodo, México ha emitido 0,2 kilos por
	cada 1.000$ de PIB, un aumento respecto a 2021.
</p>
<p>
	Aunque las emisiones totales de dióxido de carbono han disminuido respecto a 2017, las emisiones
	por habitante han aumentado en este mismo periodo.
</p>

